package com.github.commons.model;

/**
 * @author: xiaofeng.zhouxf
 * @author mercy
 * @version 1.0.13 Aug 7, 2009
 * @since 1.0.13
 */
public interface Observable<T> {
	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Checks the Observable is changed or not.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 * @return
	 */
	boolean isChanged();

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Adds an Observer instance.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 * @param observer
	 */
	void addObserver(Observer<T> observer);

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Deletes an Observer instance.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 * @param observer
	 */
	void deleteObserver(Observer<T> observer);

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Notifies Observers with null.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 */
	public void notifyObservers();

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Notifies Observers
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 * @param arg
	 */
	public void notifyObservers(T arg);

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Delete all Observers instance.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 */
	void deleteObservers();

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Gets the count of observers.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 * @return
	 */
	int countObservers();

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Sets changed state.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 */
	void setChanged();

	// --------------------------------------------------------------------------------------------
	// Instance Method
	// --------------------------------------------------------------------------------------------
	/**
	 * Clears changed state.
	 * 
	 * @since 1.0.13 Aug 7, 2009
	 */
	void clearChanged();
}
